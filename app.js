

var sehwagImage = document.querySelector('.sehwag');
var sehwagName = document.querySelector('.SehwagName');

sehwagImage.style.visibility = "hidden";

sehwagName.addEventListener("click", function() {
    if (sehwagImage.style.visibility == "hidden") {
        sehwagImage.style.visibility = "visible";
    } else {
        sehwagImage.style.visibility = "hidden";
    }
});



